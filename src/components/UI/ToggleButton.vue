<template>
  <div class="toggle-button">
    <label class="switch">
      <input type="checkbox" v-model="checked" @change="onChange" />
      <span class="slider round"></span>
    </label>
  </div>
</template>

<script>
import { ref } from "vue";
export default {
  props: {
    value: {
      type: Boolean,
      default: false
    }
  },
  setup(props, { emit }) {
    const checked = ref();
    checked.value = props.value;
    function onChange() {
      emit("onchange", checked.value);
    }

    return { checked, onChange };
  }
};
</script>

